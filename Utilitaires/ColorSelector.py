from enum import Enum

"""Type Enum qui permet d'obtenir les couleurs en format RGB"""
class ColorSelector(Enum):
    SKY_BLUE = (135, 206, 235)
    BLUE = (50, 150, 255)
    GREEN = (50, 200, 50)
    RED = (220, 60, 60)
    YELLOW = (255, 215, 0)
    PURPLE = (150, 80, 200)
    BROWN = (120, 70, 30)
    BLACK = (0, 0, 0)
    WHITE = (255, 255, 255)
